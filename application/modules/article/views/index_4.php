							<h2 class="font-weight-bold"><a href="blog-post.html">Rekomendasi Surga Wisata Untuk Sahabat ITX</a></h2>
				
							<div class="post-meta">
								<span><i class="far fa-user"></i> By <a href="#">Tia Octavianti Yusuf</a> </span>
