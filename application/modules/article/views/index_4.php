								<p>
									Apa yang dilakukan Sahabat ITX saat libur Lebaran Sekolah panjang ini? Sudah adakah list destinasi yang akan dikunjungi? Kini ITX akan merekomendasikan destinasi unggulan Indonesia yang patut Sahabat ITX kunjungi.

									Banjarnegara – Jawa Tengah
									Banjanegara memiliki destinasi wisata unggulan yang bisa Sahabat ITX kunjungi loh. Salah satunya Dieng. Siapa yang tidak tahu Dieng. Dieng memiliki keindahan alam yang sanagat bagus, selain keindahan alam yang dimiliki, Dieng juga mempunyai makanan khas yaitu Carica, memiliki bentuk menyerupai pepaya, buahnya segar dan tentunya bisa dijadikan oleh-oleh khas Dieng. Selain itu Dieng juga dikenal dengan beberapa anak Dieng atau yang disebut Anak Bajang punya rambut gimbal alami.  Dipercaya, anak-anak ini adalah keturunan leluhur Dieng, yakni Kyai Kolodethe. Rambut gimbal tidak boleh dipotong sembarangan, tapi harus dengan ritual tertentu. Sebelum dipotong, keinginan Anak Bajang harus dipenuhi. Tak jarang keinginan itu sangat aneh dan tak wajar untuk seorang bocah. Sehingga membuat ketua Pokdarwis Dieng selalu membuat Event tahunan yaitu Dieng Culture Festival (DCF) yang tahun ini akan di selenggarakan 3-5 Agustus 2018 event ini berpusat untuk wisatawan bisa menyaksikan pemotongan rambut gimbal. Sahabat ITX bisa membeli tiket DCF di pointers.id sebagai salah satu distributor ITX.

									Sahabat ITX juga bisa menginap di Green Savanah Homestay, salah satu homestay mitra ITX yang memiliki reputasi baik pada tahun 2017. Selain itu Sahabat ITX juga bisa mengunjungi desa wisata yang dikelola Pokdarwis Dieng Pandawa. Pokdarwis Dieng Pandawa menjadi salah satu Desa Wisata yang aktif mengembangkan system digital bersama ITX.

									Tanjung Lesung – Banten
									Pantai Tanjung Lesung memiliki pasir putih yang sangat lembut dan memiliki gradasi warna pantai yang sangat indah. Sahabat ITX pasti akan merasa betah bila mengunjungi Pantai Tanjung Lesung. Selain itu, Pantai Tanjung Lesung juga terhitung Pantai yang cukup sepi sehingga membuat nyaman wisatawan. Sahabat ITX akan merasa memiliki Pantai pribadi. ITX mempunyai rekomendasi homestay di Tanjung Lesung yang bisa dijadikan alternative Sahabat ITX menginap. Bunar Tunggal Villa and Resto bisa dijadikan pilihan Sahabat ITX untuk menginap pada saat berlibur di Pantai Tanjung Lesung. Bunar Tunggal Villa and Resto merupakan salah satu homestay yang sudah join dengan ITX dan memiliki reservasi yang aktif di tahun 2017.  

									Malang – Jawa Timur
									Banyak destinasi wisata yang bisa menjadi pilihan Sahabat ITX apabila ke Malang. ITX akan merekomendasikan salah satu Desa Wisata yang dimiliki Malang yaitu, Desa Wisata Mulyorejo, Kecamatan Ngantang. Mulyorejo menonjolkan dan menjual potensi melalui tempat wisata Bendungan Selorejo beserta bentangan alam hijau di sekelilingnya. Seperti diketahui, wilayah Ngantang sangat strategis karena dikelilingi empat gunung. Yaitu Gunung Kelud, Gunung Kawi, Gunung Arjuno, dan Gunung Anjasmoro.

									Lokasi wisata Bendungan Selorejo berada di jalur Malang-Kediri. Waduk Selorejo juga menawarkan keindahan refleksi alam yang sempurna. Bila berada di wisata ini, Sahabat ITX akan terhipnotis oleh indahnya pemandangan.

									Karena itu, pihak desa wisata Mulyorejo berencana mengembangkan potensi wisata dengan membangun jembatan naga namun latarnya tetap view elok Waduk Selorejo. Keren bukan?

									Bantul – Jateng
									Selain Pokdarwis Dieng Pandawa di Jawa Tengah masih memiliki Desa Wisata lainnya. Salah satunya Desa Wisata Candran International. Desa wisata ini telah dikenal secara luas, bahkan hingga internasional, sebagai desa tani dengan suasana pedesaan yang sangat tenang. Sebagian besar warga di desa ini berprofesi sebagai petani serta peternak. Para warga bertani dengan cara tradisional. Jauh dari kesan modern.

									Desa Wisata Candran berada di dusun Mandingan, kelurahan Kebonagung, kecamatan Imogiri, kabupaten Bantul. Desa ini dihuni oleh sekitar 315 kepala keluarga dimana sekitar 35% dari warganya merupakan petani tradisional dan peternak. Total lahan pertanian di desa ini sekitar 20 hektare. Memasuki desa wisata ini kita akan disambut dengan suasana Jawa yang begitu terasa. Baik itu dari perilaku masyarakat maupun bangunan-bangunan rumah.

									Untuk Sahabat ITX yang bosan dengan liburan yang mainstrem, Sahabat ITX bisa menjadikan Desa Wisata Candran sebagai salah satu pilihan berlibur juga.

									Bagaimana rekomendasi dari ITX sangat keren bukan? Berlibur yang berbeda dan tidak mainstrem bisa dijadikan pilihan Sahabat ITX untuk berlibur setelah Lebaran ini. Indonesia itu keren dan luas loh Sahabat ITX, segerakan atur jadwal untuk berlibur. Dijamin rekomendasi dari ITX keren dan tentunya Sahabat ITX engga akan nyesel deh.

									Beberapa destinasi yang direkomendasikan di atas, juga cocok bagi pelajar yang akan menikmati liburan karena desa wisata dan homestay juga nyaman bagi anak-anak. Beberapa desa wisata seperti Dieng Pandawa dan Chandran juga memiliki kegiatan wisata edukasi.


