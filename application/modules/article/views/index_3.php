								<p>Indonesia saat ini sedang mengalami perubahan cuaca yang sangat ekstrim dan terdapat beberapa bencana alam yang menimpa Kota di Indonesia. Kemarin, Gunung Agung yang mengeluarkan “amarahnya”. Saat ini salah satu Daerah yang memiliki destinasi wisata yang cukup terkenal yaitu Dieng mengeluarkan embun es atau bun upas. Embun es yang muncul di musim kemarau sulit diprediksi. Saat ini suhu di  Dieng mencapai minus 5 derajat. Suhu udara di dataran tinggi Dieng diprediksi akan terus turun hingga bulan Agustus mendatang. Bahkan suhu bisa mencapai 0 derajat celsius.

								Meskipun menjadi daya tarik bagi wisatawan, fenomena embun upas yang muncul pada puncak musim kemarau merupakan musuh petani di Dataran Tinggi Dieng karena dapat merusak tanaman kentang / sayuran.

								Tanaman kentang, wortel dan jenis sayuran lainnya langsung layu dan mati meski hanya sekali terkena bun upas. Berbeda dengan tanaman carica yang tumbuh di dataran tinggi Dieng ini cenderung lebih tahan ketika terkena bun upas.

								Untuk pengunjung Dieng Culture Festival (DCF) yang akan digelar tanggal 3-5 Agustus 2018 jangan kaget bila melihat dan merasakan fenomena alam seperti ini benar adanya. Dibalik suhu udara yang teramat dingin di Dieng, disinilah pengunjung dapat menemukan sebuah keajaiban-keajaiban alam yang tercipta di negeri atas awan Dieng ini yaitu salju Dieng.

								Percaya tidak percaya Dataran Tinggi Dieng akan bersalju saat musim kemarau tiba. Karena suhu udara minus hal ini yang menyebabkan embun-embun pagi akan membeku yaitu dengan membentuk kristal-kristal es. Kristal-kristal es tampak tebal menyelimuti area Dieng Plateau. Buat kalian yang mau liburan ke Dieng jangan lupa membawa jaket, kupluk dan shall karena munculnya embun yang mengkristal ini tidak menentu sekalipun hanya ada di musim kemarau.

								Ingin melihat keajaiban-keajaiban alam yang tercipta di negeri atas awan nanti? Sahabat ITX bisa langsung beli tiket nya di www.pointers.id/event. Buruan sebelum tiket DCF habis.

								 

