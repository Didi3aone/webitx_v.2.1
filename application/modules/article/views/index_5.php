									Apa yang Sahabat ITX lakukan di bulan puasa untuk memanfaatkan waktu kosong? Banyak orang mungkin akan menjawab pergi ke mall atau nonton di Bioskop. Momen puasa tahun ini diisi tim ITX dengan mengadakan kegitan berbagi kasih di salah satu Panti di Tangerang. Tepatya tanggal 31 Mei kemarin tim ITX berkesempatan untuk mengunjungi Panti Jompo Wredha.

