								<p>Musik Jazz, siapa yang tidak tahu dengan musik jazz. Tua, muda dan dari kalangan manapun semakin banyak yang menyukai musik jazz. Indonesia memang terkenal sering mengadakan event besar dengan tema Jazz.</p>
								<p>Salah satunya Maratua Jazz and Dive Fiesta (MJDF) 2018 yang akan berlangsung dari tanggal 27-29 Juli 2018. Dan penyelenggaraan event ini sudah memasuki tahun ke 3, kali ini bertempat di Nirvana Resort Pulau Maratua, Kepulauan Derawan, Berau Kalimantan Timur, yang diperkirakaan akan disaksikan hampir 500 orang.</p>
								<p>Akan ada ragam aktifitas yang bisa Sahabat ITX nikmati seperti Jazz Concert, Dive Trip, Snorkling, Gowes Jazz, Keliling 4 Pulau Maratua-Kakaban-Sangalaki-Derawan, Food Festival, Transplantasi Karang, Pameran Foto dan masih banyak lainnya. Selain itu, Sahabat ITX juga bisa menghadiri Meet and Greet untuk berkenalan lebih dekat dengan para musisi pengisi acara Maratua Jazz and Dive Fiesta.</p>
								<p>Yang spesial Sahabat ITX bisa menikmati suara petikan Sape&rsquo; oleh Uyau Moris. Siapa dia? Anak Dayak Kenyah yang Mendunia Berkat Sape'. Sahabat ITX bisa menikmati musik yang intim ditemani bulan purnama yang bersinar dan juga di iringi suara deburan ombak di pulau terdepan Indonesia yang eksotis dan menawan .</p>
								<p>Pada tanggal 2-5 Juli 2018, Tim ITX yang di wakili Bapak Syafrul Hidayat dan dari Tim Telkom Tour and Travel Bapak Bimo, melakukan Workshop digitalisasi di Pulau Maratua yang diikuti dengan antusias oleh para pemilik homestay dan pelaku wisata lainnya.</p>
								<p>Antusiasme yang ditunjukan pelaku wisata kepada Tim ITX sangat baik sehingga cukup banyak yang mendaftar untuk diaktifkan di sistem ITX sehingga bisa dipesan secara online. Supplier baru ITX ini akan ikut meramaikan event MJDF 2018 nanti.</p>
								<p>Sahabat ITX bisa membelli tiket MJDF di <a href="https://www.pointers.id/event/detail/maratua-jazz-and-dive-fiesta-2018/">https://www.pointers.id/event/detail/maratua-jazz-and-dive-fiesta-2018/</a></p>
