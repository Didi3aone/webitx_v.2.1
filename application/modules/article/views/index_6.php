								<p>
									Ramdhan Jazz merupakan event tahunan yang diselenggarakan oleh RICMA (Remaja Islam Masjid Cut Meutia). Pagelaran Ramdhan Jazz yang ke 8 ini selalu menjadi perhatian banyak orang, khususnya yang berada di kawasan Jakarta. Tahun ini Ramdhan Jazz bertajuk Jakarta Is Us dengan tagline #LoveUnitesAll dengan membawa pesan cinta dan harmoni kedamaian bagi seluruh warga Jakarta. Dan yang lebih penting, seluruh dana dari hasil penjualan tiket yang terjual, sepenuhnya didonasikan untuk yayasan kanker CISC.

									Acara yang berlangsung dari tanggal 25-26 Mei 2018 ini dimeriahkan oleh penyanyi-penyanyi hits Indonesia. Seperti Yura Yunita, Andine, Glenn Fredly, Isyana, Afgan, Rendy Pandugo, Maliq D’Essentials dan masih banyak lagi. Animo yang diberikan sangat luar biasa terbukti di hari kedua acara jumlah pengunjung mencapai 2.000 orang.

									ITX mendapat kesempatan untuk berkolaborasi mendukung kegiatan ini dengan solusi digital platform untuk penjualan tiket secara online. Bentuk kerjasamanya sesuai dengan fungsi ITX sebagai channel manager yaitu mengkoneksikan Tiket Ramadhan Jazz sebagai produk supplier dan mengkoneksikan dengan distributor market place antara lain Pointers.id.

									Selain ITX dan Pointers dan Mobile Cloud pun ikut berpartisipasi dalam acara Ramdhan Jazz Pointers sebagai distributor ITX sukses menjual tiket Ramadhan Jazz dengan jumlah 146 tiket. Dan pada acara tanggal 25 dan 26 Mei tim ITX hadir mensupervisi kegiatan penukaran tiket (redeem) yang diwakili oleh Adryan, Enggi, Dala dan Citra sekaligus membantu memonitor administrasi penjualan tiket online tersebut bersama panitia.

									Direktur Retail Business Solution (RBS) bapak Achmad Sugiarto dan Head of Sales Marketing ITX bapak S. Hidayat sempat berdiskusi dengan bapak Agus Baisuni selaku founder Ramadhan Jazz untuk kerjasama jangka panjang pembetukan digital jazz ecosystem. Semoga tahun depan acara Ramadhan Jazz akan meningkat jumlah pengunjungnya, dan ITX pun dapat bekerjasama kembali untuk mensukseskan penjualan tiket secara online serta mewujudkan ecosystem digital komunitas pencinta jazz.


